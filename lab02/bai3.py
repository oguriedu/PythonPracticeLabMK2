x=float(input('Nhập thu(1 đến 7):'))
while True:
      if x==1:
            print('1:sunday')
      elif x==2:
            print('2:monday')
      elif x==3:
            print('3:tuesday')      
      elif x==4:
            print('4:wednesday')
      elif x==5:
            print('5:thursday')
      elif x==6:
            print('6:friday')
      elif x==7:
            print('7:saturday')
      else:
            print('Vui long nhap lai!!!')
            x=float(input('Nhập thu(1 đến 7):'))
            break